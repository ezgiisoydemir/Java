package Day14;

public class Task1_WhileLoop_PrintNumbers {

    /*

                        TASK
        Print all the numbers from 0 to 100(hundred included)
        Use While Loop!

     */


    public static void main(String[] args) {

        int counter = 0;

        while (counter <= 100) {
            System.out.println(counter);
            counter++;
        }

    }

}
