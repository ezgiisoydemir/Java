package Day14;

public class _04_ForLoop {

    public static void main(String[] args) {

        for (int counter = 0; counter < 5; counter++) {
            System.out.println("Hello World!");
        }


        for (int counter = 1; counter <= 5; counter++) {
            System.out.println("Hello World!");
        }


        for (int counter = 5; counter > 0; counter--) {
            System.out.println("Hello World!");
        }

    }

}
