package Day14;

public class Task2_WhileLoop_PrintOddNumbers {


    /*

                        TASK
        Print all the odd numbers from 1 to 100
        Use While Loop!

     */

    public static void main(String[] args) {

        int counter = 1;

        while (counter < 100) {
            System.out.println(counter);
            counter += 2; // counter = counter + 2
        }

    }


}
