package Day2;

public class Println {

public static void main(String[] args) {  // psvm -> shortcut for public static void main method

        // When we use println method
        // it will first print what we have
        // and then move to next line!
        System.out.println("Welcome"); // sout -> shortcut for println method
        System.out.println("to");
        System.out.println("java");
        System.out.println("class!");

        System.out.println(); // Leave space between numbers and sentence
        System.out.println("1");
        System.out.println("2");
        System.out.println("3");
        System.out.println("4");
    }
}
