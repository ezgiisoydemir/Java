package Day2;

public class EscapeSequences {

    public static void main(String[] args) {

        // I learn "Java" from TechnoStudy
        // To be able to use double quotes in sentence we need backslash
        System.out.println("I learn \"Java\" from TechnoStudy");

        // "Be yourself; everyone else is already taken."
        System.out.println("\"Be yourself; everyone else is already taken.\"");

        // To be use backslash in a sentence we need backslash
        System.out.println("C:\\ProgramFiles\\...");

        // To move a new line -\n
        /*
        Java is very good
        programming language
         */
        System.out.println("Java is very good\nprogramming language");

        // To use tab - \t
        System.out.println("\tMelody");
        System.out.println("Melody");
    }
}
