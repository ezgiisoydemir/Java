package Day8.StringMethodsTasks;

public class Task5 {

    //                  TASK
    // Create the following String:
        // String message = "Hello TechnoStudy";

    // convert "Hello TechnoStudy!" to lowercase letters
    // convert "Hello TechnoStudy!" to uppercase letters

    public static void main(String[] args) {

        String message = "Hello TechnoStudy!";

        String lowerCaseMessage = message.toLowerCase();
        System.out.println(lowerCaseMessage);

        String upperCaseMessage = message.toUpperCase();
        System.out.println(upperCaseMessage);

    }

}
