package Day3.PrimitiveTypes;

public class IntegerDataType {

    public static void main(String[] args) {

        int instagramLikeCount = 200_000_000;   // we use _ instead of dot

        int myFirstInt = 123_456_789;
        int mySecondInt = -987_654_321;

        System.out.println(myFirstInt);
        System.out.println(mySecondInt);

        int maxValue = 2_147_483_647;
        int minValue = -2_147_483_648;

        System.out.println(maxValue);
        System.out.println(minValue);




    }
}
