package Day16;

public class Task_NestedLoop {

    // Print a square 4X4 wide out of '*' character
    // ****
    // ****
    // ****
    // ****

    public static void main(String[] args) {


        for (int i = 0; i < 4; i++) {


            for (int j = 0; j < 4; j++){
                System.out.print("*");
            }

            System.out.println();

        }

    }

}
