package Day16.Arrays;

public class _02_JavaArray {

    public static void main(String[] args) {

//        [0, 0, 0]
        int[] myArray = new int[3];

        System.out.println(myArray[0]);
        System.out.println(myArray[1]);
        System.out.println(myArray[2]);

        myArray[0] = 5;
        myArray[1] = 3;
        myArray[2] = 25;

        System.out.println(myArray[0]);
        System.out.println(myArray[1]);
        System.out.println(myArray[2]);

    }

}
