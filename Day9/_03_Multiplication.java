package Day9;

public class _03_Multiplication {

    public static void main(String[] args) {

        // Multiplication using only values
        System.out.println(5 * 3);

        // Multiplication using only variables
        int x = 8;
        int y = 9;

        System.out.println(x * y);

        int result = x * y; // 8 * 9 - 72 | int result = 72
        System.out.println(result);

        // Multiplication between variable and value
        System.out.println(x * 2);

        x = 10;
        System.out.println(result); // 72
        System.out.println(x*y); // 90

    }

}
